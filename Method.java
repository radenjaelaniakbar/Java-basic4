public class Method {

    static void myMethod(String name, int value) {
        System.out.println(name + " is " + value);
    }

    public static void main(String[] args) {
        myMethod("yoga", 80);
        myMethod("Dani", 60);
        myMethod("Pratama", 90);
        myMethod("Buya", 77);
        myMethod("Sri", 75);
    }
}
